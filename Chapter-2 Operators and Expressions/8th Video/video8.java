public class video8 {
    public static void main(String[] args) {
        // Arithemetic Operator //
        int a = 4;
        
    // Modulo Operator //
    int b = 6 % a;

    // Assignment Operator //
    int b = 9;
    b *= 3;
    System.out.println(b);

    // Comparison Operator //
    System.out.println(64<6);

    // Logical Operator //
    System.out.println(64>5 && 64>98);
    System.out.println(64>5 || 64>98);

    // Bitwise Operator //
    System.out.println(2&3);
    }
}
